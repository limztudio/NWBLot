ArchiveSink::ArchiveSink(CaptureSession& session)
    : m_session(session)
    , m_path(session.recorder().arena())
{}

ArchiveResult ArchiveSink::flush(){
    return flush(m_options.clearAfterFlush);
}

ArchiveResult ArchiveSink::flush(const bool clearAfterWrite){
    if(!pathConfigured()){
        ArchiveResult result;
        result.status = ArchiveStatus::NotConfigured;
        return result;
    }

    return m_session.flushArchive(m_path, clearAfterWrite);
}

ArchiveResult ArchiveSink::flushIfEnabled(){
    if(!m_options.enabled){
        ArchiveResult result;
        result.status = ArchiveStatus::Disabled;
        return result;
    }

    return flush();
}


