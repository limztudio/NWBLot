[[nodiscard]] inline SkinnedGeometryValidation::RuntimePayloadFailureInfo FindRuntimeMeshPayloadFailure(const SkinnedGeometryRuntimeMeshInstance& instance){
    return SkinnedGeometryValidation::FindRuntimePayloadFailure(
        SkinnedGeometryValidation::RuntimePayloadArrays{
            instance.restVertices,
            instance.indices,
            instance.sourceTriangleCount,
            instance.skeletonJointCount,
            instance.skin,
            instance.inverseBindMatrices,
            instance.sourceSamples,
            instance.editMaskPerTriangle,
            instance.morphs
        }
    );
}

[[nodiscard]] inline bool ValidRuntimeMeshPayloadArrays(const SkinnedGeometryRuntimeMeshInstance& instance){
    return FindRuntimeMeshPayloadFailure(instance).reason == SkinnedGeometryValidation::RuntimePayloadFailure::None;
}


