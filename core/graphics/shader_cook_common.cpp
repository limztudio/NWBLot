bool ParseManifestFile(const Path& manifestPath, ManifestData& outManifest, AString& outError){
    outManifest = {};
    outManifest.volumeName = "graphics";
    outManifest.segmentSize = s_DefaultSegmentSize;
    outManifest.metadataSize = s_DefaultMetadataSize;

    AString manifestText;
    if(!ReadTextFile(manifestPath, manifestText)){
        outError = StringFormat("Failed to read manifest '{}'", PathToString(manifestPath));
        return false;
    }
    StripUtf8Bom(manifestText);

    bool parsingEntry = false;
    ManifestEntry currentEntry;

    AStringStream stream(manifestText);
    AString line;
    usize lineNumber = 0;
    while(ReadTextLine(stream, line)){
        ++lineNumber;

        TrimTrailingCarriageReturn(line);

        const AString trimmed = Trim(line);
        if(trimmed.empty())
            continue;
        if(trimmed[0] == '#')
            continue;

        if(trimmed == "entry.begin"){
            if(parsingEntry){
                outError = StringFormat("Manifest line {}: nested entry.begin is not allowed", lineNumber);
                return false;
            }

            parsingEntry = true;
            currentEntry = {};
            currentEntry.compiler = "glslang";
            currentEntry.entryPoint = "main";
            continue;
        }
        if(trimmed == "entry.end"){
            if(!parsingEntry){
                outError = StringFormat("Manifest line {}: entry.end without entry.begin", lineNumber);
                return false;
            }

            if(currentEntry.name.empty() || currentEntry.stage.empty() || currentEntry.source.empty()){
                outError = StringFormat(
                    "Manifest line {}: entry requires name/stage/source (entry.name, entry.stage, entry.source)",
                    lineNumber
                );
                return false;
            }

            currentEntry.name = CanonicalizeText(currentEntry.name);
            currentEntry.compiler = currentEntry.compiler.empty() ? AString("glslang") : CanonicalizeText(currentEntry.compiler);
            currentEntry.stage = CanonicalizeText(currentEntry.stage);
            currentEntry.targetProfile = CanonicalizeText(currentEntry.targetProfile);
            currentEntry.defaultVariant = currentEntry.defaultVariant.empty()
                ? AString()
                : CanonicalizeText(currentEntry.defaultVariant);

            outManifest.entries.push_back(currentEntry);
            parsingEntry = false;
            continue;
        }

        const usize equalPos = trimmed.find('=');
        if(equalPos == AString::npos){
            outError = StringFormat("Manifest line {}: expected key=value", lineNumber);
            return false;
        }

        const AString key = Trim(AStringView(trimmed).substr(0, equalPos));
        const AString value = Trim(AStringView(trimmed).substr(equalPos + 1));
        if(key.empty()){
            outError = StringFormat("Manifest line {}: key is empty", lineNumber);
            return false;
        }

        if(!parsingEntry){
            if(key == "volume.name"){
                outManifest.volumeName = CanonicalizeText(value);
                continue;
            }
            if(key == "volume.segment_size"){
                u64 parsedValue = 0;
                if(!ParseU64(value, parsedValue) || parsedValue == 0){
                    outError = StringFormat("Manifest line {}: invalid volume.segment_size '{}'", lineNumber, value);
                    return false;
                }
                outManifest.segmentSize = parsedValue;
                continue;
            }
            if(key == "volume.metadata_size"){
                u64 parsedValue = 0;
                if(!ParseU64(value, parsedValue)){
                    outError = StringFormat("Manifest line {}: invalid volume.metadata_size '{}'", lineNumber, value);
                    return false;
                }
                outManifest.metadataSize = parsedValue;
                continue;
            }
            if(key == "include_root"){
                if(value.empty()){
                    outError = StringFormat("Manifest line {}: include_root cannot be empty", lineNumber);
                    return false;
                }
                outManifest.includeRoots.push_back(value);
                continue;
            }

            outError = StringFormat("Manifest line {}: unsupported global key '{}'", lineNumber, key);
            return false;
        }

        if(key == "entry.name"){
            currentEntry.name = value;
            continue;
        }
        if(key == "entry.compiler"){
            currentEntry.compiler = value;
            continue;
        }
        if(key == "entry.stage"){
            currentEntry.stage = value;
            continue;
        }
        if(key == "entry.target_profile"){
            currentEntry.targetProfile = value;
            continue;
        }
        if(key == "entry.entry_point"){
            currentEntry.entryPoint = value.empty() ? AString("main") : value;
            continue;
        }
        if(key == "entry.source"){
            currentEntry.source = value;
            continue;
        }
        if(key == "entry.default_variant"){
            currentEntry.defaultVariant = value;
            continue;
        }

        static constexpr AStringView s_EntryDefinePrefix = "entry.define.";
        if(key.starts_with(s_EntryDefinePrefix)){
            const AString defineName = Trim(AStringView(key).substr(s_EntryDefinePrefix.size()));
            if(defineName.empty()){
                outError = StringFormat("Manifest line {}: define key is empty", lineNumber);
                return false;
            }

            CookVector<AString> defineValues;
            if(!ParseDefineValues(value, defineValues)){
                outError = StringFormat("Manifest line {}: define '{}' has invalid values '{}'", lineNumber, defineName, value);
                return false;
            }

            currentEntry.defineValues[defineName] = defineValues;
            continue;
        }

        outError = StringFormat("Manifest line {}: unsupported entry key '{}'", lineNumber, key);
        return false;
    }

    if(parsingEntry){
        outError = "Manifest parsing failed: missing trailing entry.end";
        return false;
    }
    if(outManifest.volumeName.empty()){
        outError = "Manifest parsing failed: volume.name is empty";
        return false;
    }
    if(outManifest.segmentSize == 0){
        outError = "Manifest parsing failed: volume.segment_size is zero";
        return false;
    }
    if(outManifest.entries.empty()){
        outError = "Manifest parsing failed: no entry was defined";
        return false;
    }
    if(!ValidateManifestEntries(outManifest.entries, outError))
        return false;

    return true;
}


bool GatherShaderDependencies(const Path& sourcePath, const CookVector<Path>& includeDirectories, CookVector<Path>& outDependencies, AString& outError){
    outDependencies.clear();

    CookHashSet<AString> visited;
    if(!CollectDependenciesRecursive(sourcePath, includeDirectories, visited, outDependencies, outError))
        return false;

    return true;
}


bool ComputeSourceChecksum(const ManifestEntry& entry, const AStringView variantName, const CookVector<Path>& dependencies, u64& outChecksum, AString& outError){
    ErrorCode errorCode;

    outChecksum = FNV64_OFFSET_BASIS;

    AString header;
    header.reserve(
        entry.name.size()
        + entry.compiler.size()
        + entry.stage.size()
        + entry.targetProfile.size()
        + entry.entryPoint.size()
        + variantName.size()
        + 6
    );
    header += entry.name;
    header += '\n';
    header += entry.compiler;
    header += '\n';
    header += entry.stage;
    header += '\n';
    header += entry.targetProfile;
    header += '\n';
    header += entry.entryPoint;
    header += '\n';
    header += variantName;
    header += '\n';

    outChecksum = UpdateFnv64(outChecksum, reinterpret_cast<const u8*>(header.data()), header.size());

    CookVector<SortedDependencyItem> sortedDependencies;
    sortedDependencies.reserve(dependencies.size());
    for(const Path& dependency : dependencies){
        SortedDependencyItem item;
        item.canonicalPath = CanonicalizeText(PathToString(dependency));
        item.path = dependency;
        sortedDependencies.push_back(Move(item));
    }

    Sort(
        sortedDependencies.begin(),
        sortedDependencies.end(),
        [](const SortedDependencyItem& lhs, const SortedDependencyItem& rhs){
            return lhs.canonicalPath < rhs.canonicalPath;
        }
    );

    for(const SortedDependencyItem& item : sortedDependencies){
        AString dependencyPrefix = item.canonicalPath;
        dependencyPrefix += '\n';
        outChecksum = UpdateFnv64(
            outChecksum,
            reinterpret_cast<const u8*>(dependencyPrefix.data()),
            dependencyPrefix.size()
        );

        CookVector<u8> dependencyBytes;
        if(!ReadBinaryFile(item.path, dependencyBytes, errorCode)){
            outError = StringFormat("Failed to read dependency file '{}'", item.canonicalPath);
            return false;
        }
        if(!dependencyBytes.empty()){
            outChecksum = UpdateFnv64(
                outChecksum,
                dependencyBytes.data(),
                dependencyBytes.size()
            );
        }

        const u8 zeroByte = 0;
        outChecksum = UpdateFnv64(outChecksum, &zeroByte, 1);
    }

    return true;
}


