#pragma once


#include "atomic.h"


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


template<typename Tag>
class TypeCounter{
public:
    template<typename T>
    static usize id(){
        static const usize value = s_NextId.fetch_add(1, MemoryOrder::relaxed);
        return value;
    }


private:
    inline static Atomic<usize> s_NextId{ 0 };
};


