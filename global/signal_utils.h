#pragma once


#include <csignal>

#include "type.h"


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


using SignalAtomic = std::sig_atomic_t;
using SignalHandler = void (*)(i32);


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


namespace ProcessSignal{


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


enum Enum : i32{
    Interrupt = SIGINT,
    Terminate = SIGTERM,
};


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


};


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


[[nodiscard]] inline bool SetSignalHandler(ProcessSignal::Enum signalValue, SignalHandler handler){
    return std::signal(signalValue, handler) != SIG_ERR;
}


