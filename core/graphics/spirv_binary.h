inline constexpr u32 s_Magic = 0x07230203u;
inline constexpr usize s_WordBytes = sizeof(u32);
inline constexpr usize s_HeaderWords = 5u;

[[nodiscard]] inline bool IsValidBytecodeSize(const usize byteSize){
    return byteSize >= s_WordBytes && (byteSize % s_WordBytes) == 0u;
}


