namespace Aftermath{


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


// NVIDIA Nsight Aftermath GPU crash dump integration. Aftermath collects crash dumps for the whole process,
// so these entry points are process-global rather than per-device. The runtime is loaded dynamically, so a
// build that vendors the SDK still has no hard dependency on the NVIDIA runtime: everything degrades to
// inactive when the runtime is missing or the selected GPU is not NVIDIA.

// Non-owning view into the collected '.nv-gpudmp' bytes. Owned by the Aftermath module and valid until the
// next dump is collected; on device-lost (a terminal event) it stays valid for the crash dispatch.
struct GpuCrashDumpView{
    const u8* data = nullptr;
    usize size = 0u;
};


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


// Enable Aftermath GPU crash dump collection for Vulkan. Idempotent and process-global; must be called
// before the Vulkan device is created. Returns true if Aftermath is active afterwards.
bool Initialize();

// Disable Aftermath GPU crash dump collection. Safe to call when inactive.
void Shutdown();

[[nodiscard]] bool IsActive();

// On device-lost, block (bounded) until Aftermath has finished collecting the crash dump, then return a
// view of the collected dump bytes (empty if Aftermath is inactive or produced no dump).
[[nodiscard]] GpuCrashDumpView WaitForCrashDump();


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


};


