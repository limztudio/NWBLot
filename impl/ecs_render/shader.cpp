bool RendererSystem::loadShader(
    Core::ShaderHandle& outShader,
    const Name& shaderName,
    const AStringView variantName,
    const Core::ShaderType::Mask shaderType,
    const Name& debugName,
    const Name* archiveStageName
){
    return ShaderAssetLoader::Load(
        outShader,
        shaderName,
        variantName,
        shaderType,
        debugName,
        m_graphics,
        m_assetManager,
        m_shaderPathResolver,
        NWB_TEXT("RendererSystem"),
        archiveStageName
    );
}


bool RendererSystem::loadDeferredCompositeVertexShader(){
    return loadShader(
        m_deferredCompositeVertexShader,
        ECSRenderDetail::s_DeferredCompositeVertexShaderName,
        Core::ShaderArchive::s_DefaultVariant,
        Core::ShaderType::Vertex,
        "ECSRender_DeferredCompositeVS"
    );
}


