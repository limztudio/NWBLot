struct FpsCameraControllerComponent{
    f32 yawRadians = 0.0f;
    f32 pitchRadians = 0.0f;
    f32 moveSpeed = 2.5f;
    f32 boostMultiplier = 4.0f;
    f32 mouseSensitivityRadiansPerPixel = SourceMath::ConvertToRadians(0.12f);
    f32 pitchLimitRadians = SourceMath::ConvertToRadians(89.0f);
};

static_assert(
    IsStandardLayout_V<FpsCameraControllerComponent>,
    "FpsCameraControllerComponent must stay layout-stable for ECS storage"
);
static_assert(
    IsTriviallyCopyable_V<FpsCameraControllerComponent>,
    "FpsCameraControllerComponent must stay cheap to move in dense ECS storage"
);
static_assert(
    sizeof(FpsCameraControllerComponent) == sizeof(f32) * 6,
    "FpsCameraControllerComponent must only contain controller scalar state"
);


