static bool ParseDefineValues(const AStringView valueText, CookVector<AString>& outValues){
    outValues.clear();

    usize begin = 0;
    while(begin <= valueText.size()){
        usize commaPos = valueText.find(',', begin);
        if(commaPos == AStringView::npos)
            commaPos = valueText.size();

        const AString value = Trim(valueText.substr(begin, commaPos - begin));
        if(value.empty())
            return false;

        outValues.push_back(value);
        if(commaPos == valueText.size())
            break;

        begin = commaPos + 1;
    }

    return true;
}


static bool ValidateDefaultVariant(const ManifestEntry& entry, AString& outError){
    if(entry.defaultVariant.empty())
        return true;

    if(entry.defaultVariant == "default"){
        if(entry.defineValues.empty())
            return true;

        outError = StringFormat(
            "Manifest parsing failed: entry '{}' default variant '{}' is not generated by entry.define.* values",
            entry.name,
            entry.defaultVariant
        );
        return false;
    }

    if(entry.defineValues.empty()){
        outError = StringFormat(
            "Manifest parsing failed: entry '{}' default variant '{}' is not generated by entry.define.* values",
            entry.name,
            entry.defaultVariant
        );
        return false;
    }

    CookHashSet<AString> seenDefines;
    usize begin = 0;
    while(begin < entry.defaultVariant.size()){
        usize segmentEnd = entry.defaultVariant.find(';', begin);
        if(segmentEnd == AString::npos)
            segmentEnd = entry.defaultVariant.size();

        const AString segment = Trim(AStringView(entry.defaultVariant).substr(begin, segmentEnd - begin));
        if(segment.empty()){
            outError = StringFormat(
                "Manifest parsing failed: entry '{}' default variant '{}' has invalid empty assignment",
                entry.name,
                entry.defaultVariant
            );
            return false;
        }

        const usize equalPos = segment.find('=');
        if(equalPos == AString::npos || equalPos == 0 || equalPos + 1 >= segment.size()){
            outError = StringFormat(
                "Manifest parsing failed: entry '{}' default variant '{}' has invalid assignment '{}'",
                entry.name,
                entry.defaultVariant,
                segment
            );
            return false;
        }

        const AString defineName = Trim(AStringView(segment).substr(0, equalPos));
        const AString defineValue = Trim(AStringView(segment).substr(equalPos + 1));
        if(defineName.empty() || defineValue.empty()){
            outError = StringFormat(
                "Manifest parsing failed: entry '{}' default variant '{}' has invalid assignment '{}'",
                entry.name,
                entry.defaultVariant,
                segment
            );
            return false;
        }
        const auto defineIt = entry.defineValues.find(defineName);
        if(defineIt == entry.defineValues.end()){
            outError = StringFormat(
                "Manifest parsing failed: entry '{}' default variant '{}' references unknown define '{}'",
                entry.name,
                entry.defaultVariant,
                defineName
            );
            return false;
        }

        bool valueFound = false;
        for(const AString& allowedValue : defineIt.value()){
            if(allowedValue == defineValue){
                valueFound = true;
                break;
            }
        }
        if(!valueFound){
            outError = StringFormat(
                "Manifest parsing failed: entry '{}' default variant '{}' has unsupported value '{}' for define '{}'",
                entry.name,
                entry.defaultVariant,
                defineValue,
                defineName
            );
            return false;
        }

        if(!seenDefines.insert(defineName).second){
            outError = StringFormat(
                "Manifest parsing failed: entry '{}' default variant '{}' assigns define '{}' more than once",
                entry.name,
                entry.defaultVariant,
                defineName
            );
            return false;
        }

        begin = segmentEnd + 1;
    }

    if(seenDefines.size() != entry.defineValues.size()){
        outError = StringFormat(
            "Manifest parsing failed: entry '{}' default variant '{}' must assign all defines",
            entry.name,
            entry.defaultVariant
        );
        return false;
    }

    return true;
}


static bool ValidateManifestEntries(const CookVector<ManifestEntry>& entries, AString& outError){
    CookHashSet<Name> uniqueEntryNames;
    for(const ManifestEntry& entry : entries){
        const Name entryName(entry.name.c_str());
        if(!uniqueEntryNames.insert(entryName).second){
            outError = StringFormat(
                "Manifest parsing failed: duplicated entry.name '{}' is not allowed",
                entry.name
            );
            return false;
        }
        if(!ValidateDefaultVariant(entry, outError))
            return false;
    }

    return true;
}


static bool ExtractIncludeName(const AStringView line, AString& outIncludeName){
    outIncludeName.clear();

    usize cursor = 0;
    while(cursor < line.size() && IsAsciiSpace(line[cursor]))
        ++cursor;

    if(cursor >= line.size() || line[cursor] != '#')
        return false;

    ++cursor;
    while(cursor < line.size() && IsAsciiSpace(line[cursor]))
        ++cursor;

    static constexpr AStringView s_IncludeKeyword = "include";
    if(line.substr(cursor, s_IncludeKeyword.size()) != s_IncludeKeyword)
        return false;
    cursor += s_IncludeKeyword.size();

    while(cursor < line.size() && IsAsciiSpace(line[cursor]))
        ++cursor;

    if(cursor >= line.size() || line[cursor] != '"')
        return false;
    ++cursor;

    const usize closingQuote = line.find('"', cursor);
    if(closingQuote == AStringView::npos || closingQuote <= cursor)
        return false;

    outIncludeName = AString(line.substr(cursor, closingQuote - cursor));
    return !outIncludeName.empty();
}


static bool ResolveIncludeFile(const AStringView includeName, const Path& sourceDirectory, const CookVector<Path>& includeDirectories, Path& outPath){
    ErrorCode errorCode;

    const Path localCandidate = (sourceDirectory / Path(includeName)).lexically_normal();
    if(FileExists(localCandidate, errorCode)){
        outPath = localCandidate;
        return true;
    }

    for(const Path& includeDirectory : includeDirectories){
        const Path includeCandidate = (includeDirectory / Path(includeName)).lexically_normal();
        if(FileExists(includeCandidate, errorCode)){
            outPath = includeCandidate;
            return true;
        }
    }

    return false;
}


static bool CollectDependenciesRecursive(const Path& dependencyPath, const CookVector<Path>& includeDirectories, CookHashSet<AString>& inOutVisitedPaths, CookVector<Path>& inOutDependencies, AString& outError){
    ErrorCode errorCode;

    const Path absolutePath = AbsolutePath(dependencyPath, errorCode).lexically_normal();
    if(errorCode){
        outError = StringFormat(
            "Failed to resolve dependency path '{}' : {}",
            PathToString(dependencyPath),
            errorCode.message()
        );
        return false;
    }

    const AString canonicalPathKey = CanonicalizeText(PathToString(absolutePath));
    if(!inOutVisitedPaths.insert(canonicalPathKey).second)
        return true;

    AString sourceText;
    if(!ReadTextFile(absolutePath, sourceText)){
        outError = StringFormat("Failed to read dependency '{}'", PathToString(absolutePath));
        return false;
    }

    inOutDependencies.push_back(absolutePath);

    AStringStream sourceStream(sourceText);
    AString line;
    while(ReadTextLine(sourceStream, line)){
        TrimTrailingCarriageReturn(line);

        AString includeName;
        if(!ExtractIncludeName(line, includeName))
            continue;

        Path includePath;
        if(!ResolveIncludeFile(includeName, absolutePath.parent_path(), includeDirectories, includePath)){
            outError = StringFormat(
                "Unable to resolve include '{}' from '{}'",
                includeName,
                PathToString(absolutePath)
            );
            return false;
        }

        if(!CollectDependenciesRecursive(includePath, includeDirectories, inOutVisitedPaths, inOutDependencies, outError))
            return false;
    }

    return true;
}


