template<typename T, typename U>
inline T checked_cast(U u){
    static_assert(!IsSame<T, U>::value, "Unnecessary checked_cast");
    if(!u)
        return nullptr;
    T t = static_cast<T>(u);
#ifdef NWB_DEBUG
    NWB_ASSERT(t);
#endif
    return t;
}


