namespace SkeletonBinaryPayload{


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


inline constexpr u32 s_SkeletonMagic = 0x534B4C31u; // SKL1

struct HeaderBinary{
    u32 magic = s_SkeletonMagic;
    u32 padding0 = 0u;
    u64 jointCount = 0u;
};
static_assert(IsStandardLayout_V<HeaderBinary>, "Skeleton header must stay binary-serializable");
static_assert(IsTriviallyCopyable_V<HeaderBinary>, "Skeleton header must stay binary-serializable");

struct JointBinary{
    NameHash nameHash = {};
    u32 parentIndex = 0u;
    u32 padding0 = 0u;
    u32 padding1[2u] = {};
    SkeletonJointMatrix localBindPose = {};
};
static_assert(IsStandardLayout_V<JointBinary>, "Skeleton joint must stay binary-serializable");
static_assert(IsTriviallyCopyable_V<JointBinary>, "Skeleton joint must stay binary-serializable");


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


};


