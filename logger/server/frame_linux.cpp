namespace __hidden_frame{


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


static Futex s_PrintMutex;
static volatile SignalAtomic s_ShouldExit = 0;

static void SignalHandler(i32){
    s_ShouldExit = 1;
}


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


};


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


void Frame::print(BasicStringView<tchar> str, Log::Type type){
    ScopedLock lock(__hidden_frame::s_PrintMutex);

    auto& stream = (type == Log::Type::Error || type == Log::Type::Fatal) ? NWB_TCERR : NWB_TCOUT;
    stream << str << static_cast<tchar>('\n');
    stream.flush();
}

Frame::Frame(void*){
}
Frame::~Frame(){
    cleanup();
}

bool Frame::init(){
    __hidden_frame::s_ShouldExit = 0;

    if(!SetSignalHandler(ProcessSignal::Interrupt, __hidden_frame::SignalHandler))
        return false;
    if(!SetSignalHandler(ProcessSignal::Terminate, __hidden_frame::SignalHandler))
        return false;

    return startup();
}
bool Frame::showFrame(){
    NWB_TCOUT << NWB_TEXT("nwb_logserver is running in console mode. Press Ctrl+C to exit.") << static_cast<tchar>('\n');
    return true;
}
bool Frame::mainLoop(){
    Timer lateTime(TimerNow());

    while(!__hidden_frame::s_ShouldExit){
        Timer currentTime(TimerNow());
        const f32 delta = DurationInSeconds<f32>(currentTime, lateTime);
        lateTime = currentTime;

        if(!update(delta))
            return false;

        SleepMS(50);
    }

    return true;
}


