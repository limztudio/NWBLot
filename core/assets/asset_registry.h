class AssetRegistry final : NoCopy{
private:
    using CodecMap = AssetMap<AString, const IAssetCodec*>;


public:
    bool registerCodec(const IAssetCodec& codec, bool replaceExisting = false);
    bool unregisterCodec(AStringView assetType);

    [[nodiscard]] const IAssetCodec* findCodec(AStringView assetType)const;

    bool deserializeAsset(
        AStringView assetType,
        AStringView virtualPath,
        const AssetBytes& binary,
        UniquePtr<IAsset>& outAsset,
        AString& outError
    )const;

#if defined(NWB_COOK)
    bool serializeAsset(
        const IAsset& asset,
        AssetBytes& outBinary,
        AString& outError
    )const;
#endif


private:
    mutable Futex m_mutex;
    CodecMap m_codecs;
};


