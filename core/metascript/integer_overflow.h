[[nodiscard]] inline bool NegateI64Overflows(const i64 value){
    return value == Limit<i64>::s_Min;
}

[[nodiscard]] inline bool AddI64Overflows(const i64 lhs, const i64 rhs){
    if(rhs > 0)
        return lhs > Limit<i64>::s_Max - rhs;
    if(rhs < 0)
        return lhs < Limit<i64>::s_Min - rhs;
    return false;
}

[[nodiscard]] inline bool SubtractI64Overflows(const i64 lhs, const i64 rhs){
    if(rhs > 0)
        return lhs < Limit<i64>::s_Min + rhs;
    if(rhs < 0)
        return lhs > Limit<i64>::s_Max + rhs;
    return false;
}

[[nodiscard]] inline bool MultiplyI64Overflows(const i64 lhs, const i64 rhs){
    if(lhs == 0 || rhs == 0)
        return false;

    if(lhs > 0){
        if(rhs > 0)
            return lhs > Limit<i64>::s_Max / rhs;
        return rhs < Limit<i64>::s_Min / lhs;
    }

    if(rhs > 0)
        return lhs < Limit<i64>::s_Min / rhs;

    return rhs < Limit<i64>::s_Max / lhs;
}

[[nodiscard]] inline bool DivideI64Overflows(const i64 lhs, const i64 rhs){
    return lhs == Limit<i64>::s_Min && rhs == -1;
}


