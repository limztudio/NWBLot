template <typename T>
constexpr inline T floorlog2(T x){ return (x == 1) ? 0 : (1 + floorlog2<T>(x >> 1)); }
template <typename T>
constexpr inline T ceillog2(T x){ return (x == 1) ? 0 : (floorlog2<T>(x - 1) + 1); }


