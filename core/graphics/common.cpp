void RenderPassOutput::addColor(TextureFormat format){
    if(numColors >= LengthOf(formatColors)){
        NWB_LOGGER_ERROR(NWB_TEXT("RenderPassOutput::addColor: Too many color attachments"));
        return;
    }

    formatColors[numColors++] = format;
}


