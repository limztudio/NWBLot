struct ShaderStageKey{
    Name shaderName = NAME_NONE;
    Name stageName = NAME_NONE;
};

struct ShaderStageKeyHasher{
    usize operator()(const ShaderStageKey& key)const{
        usize seed = Hasher<Name>{}(key.shaderName);
        ::HashCombine(seed, key.stageName);
        return seed;
    }
};

inline bool operator==(const ShaderStageKey& lhs, const ShaderStageKey& rhs){
    return lhs.shaderName == rhs.shaderName && lhs.stageName == rhs.stageName;
}


