inline constexpr usize s_DumpArenaPayloadSize = 512u * 1024u;


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


Alloc::PersistentArena& DumpArena(){
    static Alloc::PersistentArena s_Arena(CrashArenaScope::s_DumpArena, Alloc::PersistentArena::StructureAlignedSize(s_DumpArenaPayloadSize));
    return s_Arena;
}

void InitializeDumpArena()noexcept{
    const Alloc::ArenaMemoryStats stats = DumpArena().memoryStats();
    if(stats.reservedBytes != 0u)
        return;
}

template<typename ArenaT>
bool EnsureCrashSpoolDirectories(const ::Path<ArenaT>& spoolDirectory){
    ErrorCode error;
    if(!EnsureDirectories(PendingDirectory(spoolDirectory), error))
        return false;

    error.clear();
    if(!EnsureDirectories(UploadedDirectory(spoolDirectory), error))
        return false;

    error.clear();
    if(!EnsureDirectories(UploadingDirectory(spoolDirectory), error))
        return false;

    error.clear();
    return EnsureDirectories(FailedDirectory(spoolDirectory), error);
}

template bool EnsureCrashSpoolDirectories(const ::Path<Alloc::PersistentArena>& spoolDirectory);


